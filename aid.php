    <p>PrEP is a medication comprising pills that are taken if you do not have HIV but under a great risk of contracting AIDS. Often when you have a partner with HIV or end up using the same needle for injection, PrEP can be highly effective</p>
    <h4>Post Exposure Prophylaxis</h4>
    <p>PEP comprises HIV medications and pills when one has been already exposed to HIV. When you are not aware that you have been exposed to HIV, PEP is often taken as a precautionary measure. PEP is offered 72 hours from the time of exposure and taken for 28 days from the date of commencement. PEP is meant for emergency use and must be taken along with other prevention measures.</p>
