      What is the difference between cosmetic surgery and plastic surgery?
        <span class="faq-toggle">▼</span>
      </div>
      <div class="faq-answer">
      Cosmetic surgery is a unique discipline of medicine focused on enhancing appearance through surgical and medical techniques. Cosmetic surgery can be performed on all areas of the head, neck, and body. Because treated areas function properly but lack aesthetic appeal, cosmetic surgery is elected.<br>
      Plastic surgery is defined as a surgical speciality dedicated to the reconstruction of facial and body defects due to birth disorders, trauma, burns, and disease. Plastic surgery is intended to correct dysfunctional areas of the body and is reconstructive.
        </div>
    </div>

    <div class="faq">
      <div class="faq-question">
      What are the different types of plastic surgery?
        <span class="faq-toggle">▼</span>
      </div>
      <div class="faq-answer">
      There are two types of plastic surgery: cosmetic and reconstructive.
        Cosmetic surgery is used to enhance and improve the appearance of normal body structures without impaired function. This type of plastic surgery is elective and not a medical necessity.<br>
        Reconstructive surgery is used to restore function to body structures that have been damaged due to congenital deformity, developmental abnormalities, or trauma. Reconstructive surgery may also incorporate some aspects of cosmetic surgery to restore a normal appearance.
    </div>
</div>

