    <p>Celiac Disease is an illness stemming from an immune reaction against gluten consumption. Gluten is a substance present in whole grains like barley, wheat, rye and more. A person with Celiac disease is prevented from eating gluten as it triggers an allergic reaction in the small intestine. Over time, if not taken care of, the small intestine lining is damaged.</p>
