    <p>When talking about cholesterol, the first things that pop into our minds are heart attack and stroke! Because of this, cholesterol has always been in the bad light. However, we often ignore the fact that not all cholesterols. There is both good and bad cholesterol in our bodies that work differently.</p>
    <p>Here, we will learn about the difference between good and bad cholesterol and how to balance them.</p>
    <h4>What is Cholesterol?</h4>
    <p>Cholesterol is a fat-like, waxy substance in the blood and all the cells in the body. It is produced in the liver and transported through the bloodstream by the fat-protein combination known as lipoprotein. Lipoproteins are of two types: low-density lipoprotein (LDL), also known as bad cholesterol and high-density lipoprotein HDL, known as good cholesterol. Let’s find out what is good cholesterol and bad cholesterol​ in detail.</p>
    <h4>Good Cholesterol</h4>
    <p>Also known as high-density lipoprotein (HDL), good cholesterol acts as a garbage collector. It carries excessive fat from the bloodstream and transfers it to the liver, where it is processed and excreted. HDL helps reduce the build-up of plaque in the arteries that leads to heart attacks or strokes. Its anti-inflammatory properties keep cardiovascular disease at bay.</p>
<h6>Ideal levels:</h6>
<p>For men, below 40 mg/dL is considered low and above 60 mg/dL is considered as good.</p>
<p>For women, below 50 mg/dL is known as low and above 60 mg/dL is considered desirable.</p>
    <h4>Bad Cholesterol</h4>
    <p>Bad cholesterol, also known as low-density lipoprotein (LDL), carries cholesterol from the liver to the body cells, performing various functions. This is good in ideal conditions, however, too much LDL in the body contributes to the formation of plaque in the arteries, increasing the risk of heart disease, stroke and peripheral artery disease. High levels of LDL can also cause blood clots increasing the risk of cardiovascular diseases.</p>
    <h6>Ideal Levels:</h6>
    <ul class="surgery-list">
        <li>Less than 100 mg/dL is considered ideal.</li>
        <li>A reading between 130 and 159 mg/dL falls within the borderline high range.</li>
        <li>Above 160 to 189 mg/dL is high.</li>
        <li>Above 190 mg/dL is very high.</li>
    </ul>
