<div class="echo-importance">
    <h3>Why Is an Echocardiogram Important?</h3>
    <p>An echocardiogram helps detect various heart conditions, such as:</p>
    <ul>
        <li><strong>Heart valve problems:</strong> including valve narrowing (stenosis), leakage (regurgitation), or valve prolapse.</li>
        <li><strong>Heart failure:</strong> Identifying heart function problems and monitoring treatment progress.</li>
        <li><strong>Congenital heart defects:</strong> Detecting issues like holes in the heart walls (septal defects) or abnormal heart vessels.</li>
        <li><strong>Pericardial disease:</strong> Detecting fluid accumulation around the heart or other issues in the pericardium (the lining around the heart).</li>
        <li><strong>Aneurysms:</strong> An echocardiogram can help detect weakened, bulging sections of arteries or heart walls.</li>
    </ul>
</div>

<div class="echo-preparation">
    <h3>How to Prepare for an Echocardiogram</h3>
    <p>Generally, there are no major preparations required for an echocardiogram, but here are a few guidelines:</p>
    <ul>
        <li><strong>Wear comfortable clothing:</strong> You may be asked to wear a hospital gown during the procedure.</li>
        <li><strong>Avoid certain foods or drinks:</strong> For specific types of echocardiograms like stress tests, fasting or avoiding caffeine may be required.</li>
        <li><strong>Bring your medical history:</strong> Have details of your health history and any medications you are taking. This will help the technician and physician assess the results more accurately.</li>
        <li><strong>Discuss medications:</strong> If you are on medication for heart conditions or have any allergies, be sure to let the provider know.</li>
    </ul>
</div>
<div class="echo-expect">
    <h3>What to Expect During the Procedure</h3>
    <p>An echocardiogram is a simple and noninvasive procedure. Here’s what typically happens during the test:</p>
    <ul>
        <li><strong>Preparation:</strong> You’ll be asked to lie down on an exam table, and the technician will apply a gel to your chest to help the sound waves travel better.</li>
        <li><strong>Probe placement:</strong> The technician will move a small probe (called a transducer) over your chest area. The probe emits sound waves that bounce off the heart and return to create images.</li>
        <li><strong>Duration:</strong> The test typically takes between 30 minutes to an hour, depending on the type of echo being done.</li>
        <li><strong>Post-procedure:</strong> There are no side effects. You can resume normal activities right after the test.</li>
    </ul>
