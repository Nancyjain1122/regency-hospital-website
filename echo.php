<div class="echo-type">
    <h3>Types of Echocardiograms</h3>
    <p>There are different types of echocardiograms. The type you have depends on the information your health care provider needs.</p>
    <ul>
        <li> <strong>Transthoracic echocardiogram, also called a TTE -</strong> This is a standard echocardiogram. It’s also called a heart ultrasound. It’s a noninvasive way to look at blood flow through the heart and heart valves. A TTE creates pictures of the heart from outside the body. Dye, called contrast, may be given by IV. It helps the heart’s structures show up better on the images.</li>
        <li><strong>Transesophageal echocardiogram, also called a TEE - </strong> If a standard echocardiogram doesn’t provide as many details as needed, your provider may do this test. It gives a detailed look at the heart and the body’s main artery, called the aorta. A TEE creates pictures of the heart from inside the body. It’s often done to examine the aortic valve. This test shouldn’t be done if you have bleeding in the upper gastrointestinal tract or a tumor or tear in the esophagus.</li>
        <li><strong>Fetal echocardiogram - </strong> This type of echocardiogram is done during pregnancy to check the baby’s heart. It’s a noninvasive test that involves moving an ultrasound wand over the pregnant person’s belly. It lets a health care provider see the unborn baby’s heart without using surgery or X-rays.</li>
        <li><strong>Stress echocardiogram - </strong>A stress echocardiogram is done right before and after you exercise at a medical office. It checks how the heart responds to physical activity or stress. Your health care provider may order this test if they think you have coronary artery disease. If you can’t exercise, you may be given medicine to make your heart work harder.</li>
    </ul>
</div>
<div class="echoo-method">
    <div class="echo-method">
    <h3>Echocardiogram methods</h3>
    <p>There are several methods to an echocardiogram. They include:</p>
    <ul>
        <li><strong>Two-dimensional (2D) or three-dimensional (3D) echocardiogram - </strong> These images provide pictures of the heart walls and valves and of the large vessels connected to your heart. A standard echocardiogram begins with a 2D study of the heart. A 3D echocardiogram is available in some medical centers and hospitals. It’s often done to get more details about the lower left heart chamber. This chamber is the heart’s main pumping area.</li>
        <li><strong>Doppler echocardiogram - </strong> Sound waves change pitch when they bounce off blood cells moving through the heart and blood vessels. These changes are called Doppler signals. This part of the test measures the speed and direction of blood flow within the heart and vessels. It can help show blocked or leaking valves and check blood pressure in the heart arteries</li>
        <li><strong>Color flow imaging - </strong>  This displays the blood flow in the heart in color. It helps identify leaky heart valves and other changes in blood flow.
</li>
    </ul>
    </div>
